import UIKit

extension UIColor {
    class func mybgclor() -> UIColor {
        return UIColor(red: 248/255, green: 248/255, blue: 246/255, alpha: 1)
    }
    
    class func mycolor2() -> UIColor {
        return UIColor(red: 190/255, green: 0/255, blue: 47/255, alpha: 1)
    }
    
    class func mycolor3() -> UIColor {
        return UIColor(red: 84/255, green: 84/255, blue: 82/255, alpha: 1)
    }
}


